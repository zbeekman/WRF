!$OMP PARALLEL DO &
!$OMP PRIVATE ( ij, its, ite, jts, jte )
DO ij=1,grid%num_tiles
    its = grid%i_start(ij)
    IF (grid%i_end(ij) .GT. ide - 1) THEN
