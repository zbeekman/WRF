chem_tile_loop_1:DO ij=1,grid%num_tiles
    its = grid%i_start(ij)
    IF (grid%i_end(ij) .GT. ide - 1) THEN
      ite = ide - 1
    ELSE
      ite = grid%i_end(ij)
    END IF
    jts = grid%j_start(ij)
    IF (grid%j_end(ij) .GT. jde - 1) THEN
      jte = jde - 1
    ELSE
      jte = grid%j_end(ij)
    END IF
    kts = k_start
    IF (k_end .GT. kde - 1) THEN
