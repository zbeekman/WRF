    INTEGER :: branch
    REAL, DIMENSION(ims:ime, jms:jme), INTENT(INOUT) :: a_ust
    REAL, DIMENSION(ims:ime, jms:jme), INTENT(INOUT) :: a_znt
    REAL :: temp2
    REAL :: temp1
    REAL :: temp0
