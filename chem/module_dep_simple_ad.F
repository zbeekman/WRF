!--------------------------------------------------
! deposition velocities
!--------------------------------------------------
    REAL, DIMENSION(its:ite, jts:jte, num_chem), INTENT(INOUT) :: ddvel
    REAL, DIMENSION(its:ite, jts:jte, num_chem), INTENT(INOUT) :: &
