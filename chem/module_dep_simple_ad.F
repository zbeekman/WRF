!--------------------------------------------------
!  Wesely dry dposition driver
!--------------------------------------------------
!  USE module_configure
    INTEGER, INTENT(IN) :: id, julday, numgas, current_month, ids, ide, &
&    jds, jde, kds, kde, ims, ime, jms, jme, kms, kme, its, ite, jts, jte&
