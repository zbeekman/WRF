      ELSE IF (config_flags%chem_opt .EQ. dust) THEN
        CALL PUSHCONTROL3B(1)
      ELSE IF (config_flags%chem_opt .EQ. gocartracm_kpp) THEN
        CALL PUSHREAL8ARRAY(rmol, (ime-ims+1)*(jme-jms+1))
