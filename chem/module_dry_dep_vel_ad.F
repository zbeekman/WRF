      CALL A_GOCART_DRYDEP_DRIVER(dtstep, config_flags, numgas, t_phy, &
&                            moist, p8w, t8w, rmol, a_rmol, aer_res_def, &
&                            a_aer_res_def, p_phy, chem, rho_phy, dz8w, &
