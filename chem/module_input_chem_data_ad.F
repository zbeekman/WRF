          DO i0=1,ad_count
            IF (i0 .EQ. 1) CALL POPCONTROL2B(branch)
          END DO
        END IF
      ELSE
        a_convfac = 0.0_8
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        a_alt = a_alt + 10.e-5*a_chem
