      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        a_alt = a_alt + 10.e-5*a_chem
        a_chem = 0.0_8
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
