        DO k=ktf,kts,-1
          CALL POPINTEGER4(ad_from0)
          CALL POPINTEGER4(ad_to0)
          DO i=ad_to0,ad_from0,-1
            CALL POPCONTROL4B(branch)
            IF (branch .LT. 5) THEN
              IF (branch .LT. 2) THEN
                IF (branch .EQ. 0) a_chem(i, k, j) = 0.0_8
              ELSE IF (branch .EQ. 2) THEN
                a_chem(i, k, j) = 0.0_8
