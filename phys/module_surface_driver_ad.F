&                    *(1.0-xice(i, j))
                CASE (2) 
                  albedo(i, j) = albsi(i, j)*xice(i, j) + 0.08*(1.0-xice&
&                    (i, j))
                END SELECT
                emiss(i, j) = 0.98*xice(i, j) + 0.98*(1.0-xice(i, j))
