              IF (rainbl(i, j) .LT. 0.0) THEN
                rainbl(i, j) = 0.0
              ELSE
                rainbl(i, j) = rainbl(i, j)
              END IF
            END DO
          END DO
        END DO
!$OMP END PARALLEL DO
      ELSE IF (PRESENT(rainbl)) THEN
!$OMP PARALLEL DO   &
