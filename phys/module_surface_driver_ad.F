              END IF
              IF (xland(i, j) .GT. 1.5) THEN
                IF (sst(i, j) .LT. 350. .AND. sst(i, j) .GT. 250.) THEN
                  tsk(i, j) = sst(i, j)
                  tslb(i, 1, j) = sst(i, j)
                END IF
              END IF
              IF (xland(i, j) .LT. 1.5 .AND. xicem(i, j) .GE. &
&                  xice_threshold .AND. xice(i, j) .LT. xice_threshold) &
&              THEN
! sea-ice point turns to water point
                xland(i, j) = 2.
                vegfra(i, j) = 0.
                snow(i, j) = 0.
                snowc(i, j) = 0.
                tmn(i, j) = sst(i, j)
                albedo(i, j) = 0.08
