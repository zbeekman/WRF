&                  xice_threshold .AND. xicem(i, j) .LT. xice_threshold) &
&              THEN
! water point turns to sea-ice point
                xicem(i, j) = xice(i, j)
                xland(i, j) = 1.
                vegfra(i, j) = 0.
                tmn(i, j) = 271.4
